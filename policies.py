class CnnPolicy(object):

    def __init__(self, sess, ob_img_space, ob_measure_space, frame_stack, ac_space, nbatch, nsteps, reuse=False): #pylint: disable=W0613
        nh, nw, nc = ob_img_space.shape
        ob_img_shape = (nbatch, nh, nw, nc * frame_stack)
        nm = (nbatch, ob_measure_space.shape[0]) # num measurements
        nact = ac_space.shape[0]
        X = tf.placeholder(tf.float32, ob_img_shape) #obs
        X_measurements = tf.placeholder(tf.float32, shape=nm)

        with tf.variable_scope("model", reuse=reuse):
            h = tf.cast(nature_cnn(X), tf.float32, name='cast_1')
            h_measurements = tf.cast(process_measurements(X_measurements), tf.float32, name='cast_2')
            h_concat = tf.concat([h, h_measurements], axis=1, name='concat_1')
            pi = fc(h_concat, 'pi', nact, init_scale=0.01)
            vf = fc(h_concat, 'v', 1)[:,0]
            logstd = tf.get_variable(name="logstd", shape=[1, nact], initializer=tf.zeros_initializer())
        
        pdparam = tf.concat([pi, pi * 0.0 + logstd], axis=1)

        self.pdtype = make_proba_dist_type(ac_space)
        self.pd = self.pdtype.proba_distribution_from_flat(pdparam)

        a0 = self.pd.sample()
        neglogp0 = self.pd.neglogp(a0)
        self.initial_state = None

        def step(ob_img, ob_measure, *_args, **_kwargs):
            a, v, neglogp = sess.run([a0, vf, neglogp0], {X:ob_img, X_measurements:ob_measure})
            return a, v, self.initial_state, neglogp

        def value(ob_img, ob_measure, *_args, **_kwargs):
            return sess.run(vf, {X:ob_img, X_measurements:ob_measure})

        self.X = X
        self.X_measurements = X_measurements
        self.pi = pi
        self.vf = vf
        self.step = step
        self.value = value

class MlpPolicy(object):
    def __init__(self, sess, ob_space, ac_space, nbatch, nsteps, reuse=False): #pylint: disable=W0613
        ob_shape = (nbatch,) + ob_space.shape
        actdim = ac_space.shape[0]
        X = tf.placeholder(tf.float32, ob_shape, name='Ob') #obs
        with tf.variable_scope("model", reuse=reuse):
            activ = tf.tanh
            h1 = activ(fc(X, 'pi_fc1', nh=64, init_scale=np.sqrt(2)))
            h2 = activ(fc(h1, 'pi_fc2', nh=64, init_scale=np.sqrt(2)))
            pi = fc(h2, 'pi', actdim, init_scale=0.01)
            h1 = activ(fc(X, 'vf_fc1', nh=64, init_scale=np.sqrt(2)))
            h2 = activ(fc(h1, 'vf_fc2', nh=64, init_scale=np.sqrt(2)))
            vf = fc(h2, 'vf', 1)[:,0]
            logstd = tf.get_variable(name="logstd", shape=[1, actdim],
                initializer=tf.zeros_initializer())

        pdparam = tf.concat([pi, pi * 0.0 + logstd], axis=1)

        self.pdtype = make_proba_dist_type(ac_space)
        self.pd = self.pdtype.pdfromflat(pdparam)

        a0 = self.pd.sample()
        neglogp0 = self.pd.neglogp(a0)
        self.initial_state = None

        def step(ob, *_args, **_kwargs):
            a, v, neglogp = sess.run([a0, vf, neglogp0], {X:ob})
            return a, v, self.initial_state, neglogp

        def value(ob, *_args, **_kwargs):
            return sess.run(vf, {X:ob})

        self.X = X
        self.pi = pi
        self.vf = vf
        self.step = step
        self.value = value
